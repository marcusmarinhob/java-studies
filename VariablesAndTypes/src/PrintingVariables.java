
public class PrintingVariables {
	public static void main(String[] args) {
			
		/** Primitive types ***************/
		int varInt = 10;	
		byte varByte = 15;
		short varShort = 20;
		double varDouble = 30;
		float varFloat = 35.0f;
		long varLong = 40l;
		boolean varBoolean = false;
		char varChar = 'A'; // 2 bytes
		String varString = "Marcus";		
		
		System.out.println("Printing Variables ################## \n");
				
		System.out.println("varInt = " + varInt); 
		System.out.println("varByte = " + varByte); 
		System.out.println("varShort = " + varShort); 
		System.out.println("varDouble = " + varDouble); 
		System.out.println("varFloat = " + varFloat); 
		System.out.println("varLong = " + varLong); 
		System.out.println("varBoolean = " + varBoolean); 
		System.out.println("varChar = " + varChar); 
		System.out.println("varString = " + varString);
		
				
	}
}

